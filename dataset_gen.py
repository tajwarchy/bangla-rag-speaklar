def build_text(brand: str, name: str, desc: str, price: int) -> str:
    """Compose a realistic Bangla product listing blob matching sample format."""
    features = random.sample(FEATURES, k=random.randint(2, 4))
    warranty  = random.choice(WARRANTY).format(brand=brand)
    parts = [
        f"{name}।",
        f"{desc}।",
        warranty,
        *features,
        f"প্রোডাক্টটির মূল্য {price} টাকা",
    ]
    return " ".join(parts)


def generate_dataset(n: int = 5000) -> pd.DataFrame:
    """
    Generate n synthetic Bangla product records mirroring sample_products.csv.

    Columns:
        id           — integer row index
        product_name — brand + product name (Bangla)
        price_bdt    — integer price in BDT
        text         — full Bangla listing blob used for embedding
    """
    records: list[dict] = []
    for i in range(n):
        name, desc, lo, hi = random.choice(PRODUCTS)
        brand  = random.choice(BRANDS)
        price  = random.randint(int(lo), int(hi))
        text   = build_text(brand, name, desc, price)

        records.append({
            "id":           i,
            "product_name": f"{brand} {name}",
            "price_bdt":    price,
            "text":         text,
        })

    return pd.DataFrame(records)


def main() -> None:
    print("Generating 5,000-product Bangla dataset…")
    df = generate_dataset(5000)

    # Validate UTF-8
    for col in ["product_name", "text"]:
        assert df[col].str.encode("utf-8").all(), f"UTF-8 check failed: {col}"

    out = "products.csv"
    df.to_csv(out, index=False, encoding="utf-8-sig")

    print(f"\n✓ Saved {len(df)} records → {out}")
    print("\nSample rows:")
    for _, row in df.sample(3, random_state=7).iterrows():
        print(f"\n  [{row['id']}] {row['product_name']} — {row['price_bdt']} টাকা")
        print(f"  {row['text'][:120]}…")

    print(f"\nPrice stats (BDT):\n{df['price_bdt'].describe().round(1).to_string()}")


if __name__ == "__main__":
    main()